#include "libft.h"

int	ft_isascii(int c)
{
	unsigned char symb;

	if (c <= 255 && c >= 0)
	{
		symb = (unsigned char)c;
		if (symb >= 0 && symb <= 127)
			return (1);
	}
	return (0);
}
