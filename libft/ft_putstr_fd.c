#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	char	*t;

	if (s)
	{
		t = (char *)s;
		while (*(t++) != '\0')
			ft_putchar_fd(*(t - 1), fd);
	}
}
